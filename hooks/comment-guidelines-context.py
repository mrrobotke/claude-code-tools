#!/usr/bin/env python3
# comment-guidelines-context.py – Inject code comment guidelines into every prompt
import json
import sys

# Load input from stdin
try:
    input_data = json.load(sys.stdin)
except json.JSONDecodeError as e:
    print(f"Error: Invalid JSON input: {e}", file=sys.stderr)
    sys.exit(1)

prompt = input_data.get("prompt", "")

# Define your commenting guidelines as context
context = """
CODE COMMENT GUIDELINES (always apply to all code):

Core rules:
- Explain WHY not what
- Keep comments shorter than the code they describe
- Prefer better names over comments
- Write in US English, present tense, imperative mood
- Never mention AI, agents, or authors
- No emojis or emotive punctuation

Placement:
- Short purpose paragraph at top of module
- Document public APIs and exported symbols
- Annotate nonobvious business rules
- Place inline comments directly above the line they explain

Structure:
- Limit lines to 100 characters
- Start with capital letter, end full sentences with period
- Use line comments for local context

Allowed tags with required formats:
- TODO[team ABC-123 by 2025-11-15] short action
- FIXED: in ABC-456 removed legacy path
- WHY keep 3 retries to satisfy partner SLO
- INVARIANT cardLast4 is a 4 digit string

Banned words and tells:
- AI tells: generated by, large language model, boilerplate, helper by agent, simple, just, obviously, clearly
- Vague filler: improve later, handle edge cases, fix later, temporary hack
- Generic labels without action: NOTE, IMPORTANT, WARNING (without concrete rule)
- Emojis

React Native/Expo specifics:
- Document only nonstandard hook behavior and cache policy
- Annotate platform forks at decision point

RTK Query specifics:
- Explain deviations from defaults
- Document cache lifetimes and invalidation rules in header comment

Security:
- No secrets, tokens, or private endpoints in comments
- When privacy drives design, write one WHY line and link to policy
"""

# Inject context using stdout (special case for UserPromptSubmit)
print(context)

# Allow the prompt to proceed with the additional context
sys.exit(0)