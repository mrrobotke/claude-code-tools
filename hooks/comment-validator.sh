#!/usr/bin/env bash
# comment-validator.sh – Enforce production grade code commenting standards
# Validates comments in files before Write operations or EOF heredoc writes

input=$(cat)

# Extract tool name and file path
tool_name=$(echo "$input" | jq -r '.tool_name // empty')
file_path=$(echo "$input" | jq -r '.tool_input.file_path // empty')
content=$(echo "$input" | jq -r '.tool_input.content // empty')

# For Bash commands with heredoc, extract file path and content
if [[ "$tool_name" == "Bash" ]]; then
  command=$(echo "$input" | jq -r '.tool_input.command // empty')
  
  # Check if it's a heredoc write operation (cat > file << EOF or tee)
  if [[ "$command" =~ (cat[[:space:]]*\>[[:space:]]*|tee[[:space:]]+)([^[:space:]]+)[[:space:]]*\<\<[[:space:]]*EOF ]]; then
    file_path="${BASH_REMATCH[2]}"
    content=$(echo "$command" | sed -n '/<<.*EOF/,/^EOF/p' | sed '1d;$d')
  else
    # Not a heredoc write, skip validation
    exit 0
  fi
elif [[ "$tool_name" != "Write" ]]; then
  # Only validate Write tool and Bash heredoc writes
  exit 0
fi

# Skip if no content or non-source files
if [[ -z "$content" ]] || [[ ! "$file_path" =~ \.(ts|tsx|js|jsx|py|sh|yaml|yml|json)$ ]]; then
  exit 0
fi

# Validation functions
validate_banned_words() {
  local banned_pattern="generated by|large language model|helper by agent|obviously|clearly|simple|just|improve later|PHASE | phase | handle edge cases|fix later|temporary hack|pahse|perfomance|secerity"
  
  if echo "$content" | grep -iE "$banned_pattern" > /dev/null; then
    echo "❌ Banned words or AI tells detected in comments" >&2
    echo "$content" | grep -inE "$banned_pattern" | head -5 >&2
    return 1
  fi
  return 0
}

validate_todo_format() {
  # Check for TODO without proper format: TODO[team ABC-123 by YYYY-MM-DD]
  if echo "$content" | grep -E "TODO(?!\[)|TODO\[[^\]]*\](?!.*by )|TODO\[[^\]]*\](?!.*[0-9]{4}-[0-9]{2}-[0-9]{2})" > /dev/null; then
    echo "❌ TODO comments must include owner and deadline" >&2
    echo "   Format: TODO[team ABC-123 by 2025-11-15] action" >&2
    echo "$content" | grep -nE "TODO" | head -3 >&2
    return 1
  fi
  return 0
}

validate_line_length() {
  # Check for comment lines exceeding 100 characters
  local long_comments=$(echo "$content" | grep -nE "^\s*(//|#|/\*|\*)" | awk -F: 'length($2) > 100 {print NR": "$0}')
  
  if [[ -n "$long_comments" ]]; then
    echo "❌ Comment lines exceed 100 character limit" >&2
    echo "$long_comments" | head -3 >&2
    return 1
  fi
  return 0
}

validate_emojis() {
  # Check for emojis in comments
  if echo "$content" | grep -P "[\x{1F300}-\x{1F9FF}]|[\x{2600}-\x{26FF}]|[\x{2700}-\x{27BF}]" > /dev/null; then
    echo "❌ Emojis are not allowed in code comments" >&2
    return 1
  fi
  return 0
}

validate_vague_labels() {
  # Check for NOTE, IMPORTANT, WARNING without concrete action
  if echo "$content" | grep -E "(NOTE|IMPORTANT|WARNING):\s*$" > /dev/null; then
    echo "❌ Generic labels (NOTE, IMPORTANT, WARNING) must be followed by concrete rules" >&2
    echo "$content" | grep -nE "(NOTE|IMPORTANT|WARNING):\s*$" | head -3 >&2
    return 1
  fi
  return 0
}

# Run all validations
errors=0

validate_banned_words || ((errors++))
validate_todo_format || ((errors++))
validate_line_length || ((errors++))
validate_emojis || ((errors++))
validate_vague_labels || ((errors++))

if [[ $errors -gt 0 ]]; then
  cat >&2 <<EOF

📋 Code Comment Guidelines Summary:
   - Explain WHY not what
   - Use present tense, imperative mood
   - Limit lines to 100 characters
   - TODO format: TODO[team ABC-123 by 2025-11-15] action
   - Allowed tags: TODO, FIXED, WHY, INVARIANT
   - Ban AI tells, vague filler, emojis
   
File: $file_path
EOF

  cat <<EOF
{
  "hookSpecificOutput": {
    "permissionDecision": "deny",
    "permissionDecisionReason": "Code comments do not meet production standards. See validation errors above. Fix comments and try again."
  }
}
EOF
  exit 2
fi

echo "✅ Comment validation passed for $file_path" >&2
exit 0