#!/bin/bash
# Stop hook: Validate session cleanup before allowing stop
# Event: Stop

set -euo pipefail

PROJECT_DIR="/Users/antonyngigge/Ignixxion/ignixxion-apps/IgnixxionNestAPI"
ISSUES=()

# Check for debug files
DEBUG_FILES=$(find "$PROJECT_DIR" -type f \( -name "debug_*" -o -name "*_debug.*" -o -name "tmp_*" \) 2>/dev/null || true)
if [ -n "$DEBUG_FILES" ]; then
  ISSUES+=("Debug files found: $(echo "$DEBUG_FILES" | head -3 | tr '\n' ' ')")
fi

# Check for versioned files in src directory
VERSIONED_FILES=$(find "$PROJECT_DIR/src" -type f \( -name "*_v[0-9].*" -o -name "*_final.*" -o -name "*_copy.*" -o -name "*_old.*" -o -name "*_backup.*" \) 2>/dev/null || true)
if [ -n "$VERSIONED_FILES" ]; then
  ISSUES+=("Versioned files found: $(echo "$VERSIONED_FILES" | head -3 | tr '\n' ' ')")
fi

# Check recent commits for banned words
cd "$PROJECT_DIR"
BANNED_PATTERN="CLAUDE|generated by|AI agent|Claude agent"
RECENT_COMMITS=$(git log --oneline -5 2>/dev/null || true)
BANNED_COMMITS=$(echo "$RECENT_COMMITS" | grep -iE "$BANNED_PATTERN" || true)

if [ -n "$BANNED_COMMITS" ]; then
  echo "" >&2
  echo "⚠️  INVALID COMMIT MESSAGES DETECTED" >&2
  echo "" >&2
  echo "   The following recent commits contain banned words:" >&2
  echo "$BANNED_COMMITS" | sed 's/^/   • /' >&2
  echo "" >&2
  echo "   Commit messages must NOT contain: CLAUDE, 'generated by', 'AI agent'" >&2
  echo "" >&2
  echo "   Fix with: git rebase -i HEAD~5" >&2
  echo "   Then reword the affected commits" >&2
  echo "" >&2

  cat <<EOF
{
  "decision": "block",
  "reason": "Commit messages contain banned words (CLAUDE, generated by, AI agent)"
}
EOF
  exit 2
fi

# Check for uncommitted changes and auto-commit
if ! git diff-index --quiet HEAD -- 2>/dev/null; then
  echo "" >&2
  echo "Auto-committing uncommitted changes..." >&2
  
  # Stage all changes
  git add -A
  
  # Get list of modified files for commit message
  CHANGED_FILES=$(git diff --cached --name-only | head -5 | xargs basename -a 2>/dev/null || echo "multiple files")
  FILE_COUNT=$(git diff --cached --name-only | wc -l | tr -d ' ')
  
  # Create clean commit message based on changes
  if [ "$FILE_COUNT" -eq 1 ]; then
    COMMIT_MSG="Update $(echo "$CHANGED_FILES" | head -1)"
  elif [ "$FILE_COUNT" -le 3 ]; then
    COMMIT_MSG="Update $(echo "$CHANGED_FILES" | tr '\n' ', ' | sed 's/, $//')"
  else
    COMMIT_MSG="Update $FILE_COUNT files from session"
  fi
  
  # Commit changes
  git commit -m "$COMMIT_MSG" >/dev/null 2>&1
  
  echo "   ✓ Committed: $COMMIT_MSG" >&2
  echo "" >&2
fi

# If other issues found, block stop and suggest cleanup
if [ ${#ISSUES[@]} -gt 0 ]; then
  echo "" >&2
  echo "⚠️  SESSION CLEANUP REQUIRED" >&2
  echo "" >&2
  echo "   Issues:" >&2
  for issue in "${ISSUES[@]}"; do
    echo "   • $issue" >&2
  done
  echo "" >&2
  echo "   Suggested actions:" >&2
  echo "   1. Remove debug/temporary files" >&2
  echo "   2. Consolidate versioned files into original files" >&2
  echo "" >&2

  cat <<EOF
{
  "decision": "block",
  "reason": "Session has ${#ISSUES[@]} cleanup issues that need resolution"
}
EOF

  exit 2
fi

# All clean - allow stop
echo "✅ Session cleanup validated - all clear" >&2
exit 0
